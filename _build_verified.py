revised_ids = []
for finding in result["findings"]:
    fid = finding["id"]
    if fid in REVISIONS:
        rev = REVISIONS[fid]
        finding["norm_verified"] = True
        finding["norm_status"] = rev["norm_status"]
        finding["norm_revision"] = rev["norm_revision"]
        for field, value in rev.get("field_updates", {}).items():
            finding[field] = value
        revised_ids.append(fid)
    else:
        finding["norm_verified"] = True
        finding["norm_status"] = "ok"
        finding["norm_revision"] = None

